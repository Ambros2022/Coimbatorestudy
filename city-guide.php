	<h1>Transport</h1>
			
<p>Coimbatore has a well-developed transport infrastructure. The city and its suburbs is traversed using its road networks. Coimbatore is well connected by Road, Rail and Air with most cities and towns in India except through waterways. The city has fairly better transport infrastructure, though road infrastructures are not well maintained and developed according to the growing needs of transport, making traffic congestion a major problem in the city.</p>

<h3>Road</h3> 

<p>The city and metropolitan area are served by major arterial roads that run either in an east-west or north-south direction . Avinashi road is one of the city's most important arterial road. It traverses most of central and east Coimbatore (National Highway 47) connecting the metropolis of Bangalore, Chennai. The road runs in a West-east direction. Other arterials include Trichy Road (Central-Southeast), Mettupalayam Road(North-South), Sathy Road (South-North East), Palkkad Road(East-West), Pollachi Road(North-South) and Thadagam Road (east-west). Maruthamalai Road starts at the intersection of Lawley Road Junction and connects Vadavalli and extends up to Maruthamalai foothills. Other roads include 100 feet road, Bharatiyar Road, Dr.Nanjappa Road, Balasundarum Road, Cross cut Road in Gandhipuram Area, DiwhanBahadhur Road(DB Road), TV Swamy Road, Brooke Bond Road in RS Puram Areas, Race Course Road etc.</p>

<p>Most of Coimbatore's intra-city transport requirements are met by an extensive public transport. The bus service, which is run by the government-owned TNSTC Coimbatore covers the city and its suburbs. In addition to the bus service, private metered call taxis and fixed-rate tourist taxis are available at all entry points to the city like airport, bus terminus and railway stations. Vans which are run like bus services and popularly called "Maxi Cabs" also ply on many routes in the city. The outlying suburban areas of the city are also served by private mini bus company services as well as government bus transport corporations of neighbouring districts</p>

<h3>AIR</h3>

<p>Coimbatore International Airport is a major airport mostly serving Coimbatore and its suburbs in the state of Tamil Nadu, India. The Coimbatore International Airport is the second busiest airport in Tamil Nadu after Chennai International Airport, in terms of passenger traffic, cargo traffic as well as aircraft movement. The Coimbatore International Airport serves as the city's airport for both domestic and international flights. The airport consists of separate International terminal and the Domestic terminal, and handles domestic as well as international flights. The Airport lies around 18 km from the city centre and is accessible by road and rail transport services.</p>

<h3>RAILWAYS</h3>

Coimbatore Junction, also known as Kovai Junction, is the primary railway station serving the city of Coimbatore.

<h4>SUBURBAN RAILWAYS</h4>

<p>Coimbatore railways has unique infrastructure to host circular rail system as it has similar bus service inside the city limits. Another unique infrastructure that can be utilized better is the 4 radiating rail routes from Coimbatore junction and next town at 40 km each from there. It is a golden opportunity for EMUs criss crossing with best convenience and efficiency. The other junctions are located at Coimbatore North Junction (2.6 km away from CBE on the northern side), Podanur Junction (5.8 km away from CBE on the southern side)and Irugur(16 km from CBE on eastern side)</p>


