				<h2>Entrance for Architecture</h2>
				<strong>NATA</strong>
				<p>NATA is known as National Aptitude Test in Architecture. It is being conducted by the Council of Architecture at national level for admission to 5year B. Arch Degree Course at all recognized institutions across India. The test measures the aptitude of applicants for a specific field of study, i.e. Architecture, in areas like drawing and observation skills, sense of proportion, aesthetic sensitivity and critical thinking, qualities that have been acquired over a long period of time and that are related to Architecture.</p>
				<div class="courses"> <b>Eligibility for NATA</b>
