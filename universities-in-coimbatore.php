			<h2>Amrita Vishwa Vidyapeetham University, Coimbatore campus</h2>
			
<p>Amrita University is a multi-campus, multi-disciplinary research university that is accredited 'A' by NAAC and is ranked as one of the best research universities in India. The university is spread across five campuses in three states of India - Kerala, Tamil Nadu and Karnataka, with the University headquarters at Ettimadai, Coimbatore, Tamil Nadu. </p>

<p>As Amrita's first campus, Coimbatore is home to the administrative headquarters of Amrita University. Schools of Engineering and Business in addition to Departments of Social Work and Communication are located on located on this campus. Research centers include AMBE, CEN, CES and Cybersecurity.</p>

<p>Amrita School of Engineering (ASE), Coimbatore was established on October 27, 1994. It has the distinction of being Amrita's first center for higher learning. Known then as the Amrita Institute of Technology and Science, it offered B.E. programs to cater to the increasing needs of technical manpower in the country. Today the School offers B.Tech. and M. Tech. programs in various disciplines. In addition, it offers programs leading to the MCA degree. Ph.D. is offered in Engineering and Sciences.The school of Engineering have organized various national and international conferences, seminars, workshops, colloquia, contests, training programs, etc. during the last five years.</p>

<p>All the laboratories are well equipped with latest equipments and software tools required for B.Tech, M.Tech and Ph.D programs. The school has state-of-the-art computer laboratories and high-end supercomputing facilities.The school provides an excellent infrastructure including Library, Laboratories and Sports facilities.The goal of the school is to make its programs the best in the country based on a focused approach with respect to the critical areas of curriculum, teaching-learning, student support, infrastructure and research.</p>


