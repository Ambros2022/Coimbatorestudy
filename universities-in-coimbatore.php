<p>Karunya University was founded with the noble vision to raise professionals and leaders of high academic caliber and unblemished character, nurtured with a strong motivation and commitment to serve humanity. The Karunya Institute of Technology was started as a private self-financing engineering college on 4 October 1986. In 2004, the institute was awarded the Deemed to be University status in recognition of its academic excellence by the Ministry of Human Resources Development, Government of India and subsequently renamed as Karunya University under section 3 of the UGC Act, 1956 vide Notification No. 9-3-2000-U3 dated. 23.6.2004 of the Government of India.</p>

