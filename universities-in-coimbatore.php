			<h2>Dr. Ambedkar Law University</h2>
			
<p>The Tamil Nadu Dr. Ambedkar Law University was established in 1997 by the Government of Tamil Nadu by an Act of State Legislature.The University is recognized by the Bar Council of India(BCI) and University Grant Commission.</p>

<p>His Excellency Shri. K.R. Narayanan, former President of India inaugurated it on September 20, 1997, and the University started from October 3, 1997 onwards. The University was named after the architect of the Indian Constitution Dr. B.R. Ambedkar.  </p>

<p>The University is the first full-fledged Law University that offers Legal Education and its affiliated colleges in the State and the University is the first of its kind not only in the country but also in South Asia.  </p>

<p>The School of Excellence in Law established by the University at its campus since 2002 has been modeled on the lines of other National Law Schools in the country and offers three years as well as five-year integrated undergraduate courses in Law. The University has a Postgraduate and Research Centre, which offers Master of Law Degree Courses in eight different specializations, doctoral program as well as organizing research in diverse areas of law. </p>



	

