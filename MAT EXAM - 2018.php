	<h1><font color="#0191d5"> MAT Application Form </font> 2018</h1>

 

 
<p>Management Aptitude Test (MAT) is a standardised test being administered since 1988 to facilitate Business Schools (B-Schools) to screen candidates for admission to MBA and allied programmes. Govt. of India, Ministry of HRD approved MAT as a national level test in 2003. Any B-School - national or international - can consider MAT Score as an admission input based on the Score Cards issued to the candidates. The largest test of its kind in the nation, MAT will continue to be your passport to over 600 B-Schools across India.</p>
 
<h3>Eligibility</h3>
 
<p>Graduates in any discipline. Final year students of Graduate Courses can also apply.</p>
 
