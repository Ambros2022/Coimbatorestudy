				<h2>BSc/MSc CHEMISTRY</h2>
			
			
<p>Chemistry, the science that deals with the properties, composition, and structure of substances (defined as elements and compounds), the transformations they undergo, and the energy that is released or absorbed during these processes. Every substance, whether naturally occurring or artificially produced, consists of one or more of the hundred-odd species of atoms that have been identified as elements. Although these atoms, in turn, are composed of more elementary particles, they are the basic building blocks of chemical substances; there is no quantity of oxygen, mercury, or gold, for example, smaller than an atom of that substance. Chemistry, therefore, is concerned not with the subatomic domain but with the properties of atoms and the laws governing their combinations and how the knowledge of these properties can be used to achieve specific purposes.</p>

<p>The days are long past when one person could hope to have a detailed knowledge of all areas of chemistry. Those pursuing their interests into specific areas of chemistry communicate with others who share the same interests. Over time a group of chemists with specialized research interests become the founding members of an area of specialization. The areas of specialization that emerged early in the history of chemistry, such as organic, inorganic, physical, analytical, and industrial chemistry, along with biochemistry, remain of greatest general interest. There has been, however, much growth in the areas of polymer, environmental, and medicinal chemistry during the 20th century. Moreover, new specialities continue to appear, as, for example, pesticide, forensic, and computer chemistry.</p>

<p>After graduation a student can go for MSc in chemistry, this will help a student to go for GATE exam or CSIR entrance test. If you can get a good score in GATE or qualify for Junior Research fellow in CSIR. Other than this after postgraduation a student can apply for some scientist B post in some of institute like BARC or DRDO. Also after graduation you can apply for some jobs in chemical companies, of course in India the last option is not so widely opened, but earlier options are very widely accepted. You can also get good opportunity in teaching if you are keen in it. In India many IT companies are also offering the job opportunities for Chemistry graduates, apart you can apply for all top MNC's IT companies. Another option is after bachelor degree you can switch to banking and other competitive exams. You can opt for administrative competitive exam (chemistry as a main subject).</p>

<p>Investigations are on for discovery of new molecules wherein a lot of chemistry personnel support is essential in R & D. Discovery of new molecules for specific action on human diseases such as cancer, AIDS etc. has led to expansion of drug industry. Outsourcing of contract research for the bulk production of certain molecules gained momentum in India which requires well trained chemistry post graduates particularly emphasizing on experimental skills. The human genome project is the focal point of interest where, microbial, plant and animal genomes has an important impact on opening up new vistas for research. Exploring animal genome opens up unlimited scope to pursue research. Bio catalyst in stereo selective synthesis of molecules of medicinal importance is another thrust area of research. Agrochemical and pesticide, bulk drug, pharmaceutical industry also requires support from a lot of well qualified chemists. Teaching at Intermediate, graduate and post-graduate level also requires a large number of well qualified chemists at M.Sc. and Ph.D. level.</p>

