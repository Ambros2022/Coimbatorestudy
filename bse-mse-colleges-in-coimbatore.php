										<h2>BSc/MSc BOTANY</h2>
									
<p>Botany is the scientific study of plants. "Plants," to most people, means a wide range of living organisms from the smallest bacteria to the largest living things. By this definition plants include: algae, fungi, lichens, mosses, ferns, conifers and flowering plants. Today scientists believe bacteria, algae and fungi are in their own distinct kingdoms, but most general botany courses, and most Botany Departments at colleges and universities, still teach about these groups.</p>

<p>Because the field is so broad, there are many kinds of plant biologists and many different opportunities available. Botanists interested in ecology study interactions of plants with other organisms and the environment. Other field botanists search to find new species or do experiments to discover how plants grow under different conditions. Some botanists study the structure of plants. They may work in the field, concentrating on the pattern of the whole plant. Others use microscopes to study the most detailed fine structure of individual cells. Many botanists do experiments to determine how plants convert simple chemical compounds into more complex chemicals. They may even study how genetic information in DNA controls plant development. Botanists study processes that occur on a time scale ranging from fractions of a second in individual cells to those that unfold over eons of evolutionary time.</p>

<p>Among the careers available to a person who enjoys the outdoors are positions as an ecologist, taxonomist, conservationist, forester, or plant explorer. Your work may take you to foreign and exotic lands. It may allow you to live and work in the great outdoors. A person with a mathematical background might find biophysics, developmental botany, genetics, modeling, or systems ecology to be exciting fields. Someone with an interest in chemistry might become a plant physiologist, plant biochemist, molecular biologist, or chemotaxonomist. Many people do not realize that most of the basic biological processes are the same in both plants and animals. Plants, however, are easier to grow and manipulate.</p>


<p>Plant structure may appeal to a person who enjoys microscopy and the beauty of intricate form and design. A person concerned about the world food supply might study plant pathology (diseases) or plant breeding. At larger universities there are frequently separate departments specializing in different applied sub disciplines of botany. Some examples are: Agronomy (field crops), Horticulture (ornamentals, fruits and vegetables), Microbiology (microbes such as bacteria and fungi) and Plant Pathology (diseases of plants). Plant biologists who enjoy working with people have a wide range of opportunities in teaching and public service.</p>

<p>The major employers of plant biologists are educational institutions, federal and state agencies, and industries. Job opportunities usually depend upon educational training and experience. New positions in botany are expected to increase at an above-average rate through the turn of the century. Growing world population continues to increase the need for better food supplies. Environmental concerns, such as air, water and soil pollution, will create openings for ecologists in government and industry. The search for new drugs and medicines and useful genes for improving crop plants will continue to create a need for botanical explorers.
</p>

<p>Educational institutions, which employ most plant biologists, range from high schools and community colleges to universities. High schools and community colleges have few openings for those who wish to teach specialized courses and there is little time or equipment for research activity. Nevertheless, for botanists who primarily enjoy teaching, such positions are very satisfying.</p>

<p>Most positions for professional plant scientists are in colleges and universities. Almost all colleges and universities offer courses in plant science and there are faculty positions for botanists who have different specialties. In addition, educational institutions employ botanists as researchers and as administrators.
</p>

<p>Industry is the third major employer of plant biologists. Drug companies, the oil industry, the chemical industry, lumber and paper companies, seed and nursery companies, fruit growers, food companies, fermentation industries (including breweries), biological supply houses and biotechnology firms all hire men and women trained in botany.</p>

<p>There are a whole lot of opportunities for you after you have completed your M.Sc. in Botany. You can an ecologist, taxonomist, conservationist, forester or plant explorer. Research organization, herbal product companies, farm management organization and biotechnology firms also require services of botany students. You can look for teaching jobs as well.</p>

			
						
