<h3>B.COM</h3>
<p>The B.Com programme provides a good base for a range of careers. These are broadly of two types - those related to business and finance such as chartered accountancy, company secretary ship, and cost and works accountancy (CWA), banking and so on, and the non-finance fields such as law, civil services, mass communication, hotel management and so on. To enhance your qualification in accounts, you may consider courses such as CWA, or others related to finance and banking such as a diploma in banking and finance offered by a number of institutes. A candidate can look at a PG diploma in Insurance Management as insurance and risk management which is another growing field for which a B.Com degree and is useful.</p>
 
<p>Further, if a candidate is interested in non-finance related courses, there are any number of diploma programmes in mass communication, event management, travel and tourism and soon. Much depends on the student's areas of interest and the field of work they wish to focus on.</p>

<h3>MCOM</h3>
<p>There are many options for after completing M. Com, like making a career in e-commerce, international business, consulting firms, banks, multinational companies, public sector enterprises, marketing organizations, etc.</p>

<p>Most M.com candidates opt for MBA, CS, CA ,LLB ( with various specializations such as accountancy, banking and finance), retail management, insurance, chartered accountancy, company secretaryship, cost and work accountancy.</p>

<p>They will also apply for post graduate programs in mass communication, advertising, and public relations, social work, labour and social welfare, personnel management and industrial relations, hospital administration, fashion management, apparel merchandising and marketing, film editing, film direction etc. Alternatively there are many number of opportunities available in business organizations, banks, insurance, retail companies.</p>

<p>The M.Com graduates have lot of opportunities available in the accounting and finance world. You can also pursue your MBA after M.Com. MBA with M.Com will provide you an attractive career path. There is an option of distance/online learning. You can do MBA through this way without discontinuing your present studies. You can also discontinue your studies with B.Com and opt MBA for the PG program if you are able to secure admission in a reputed MBA institute. For getting admission into the MBA in recognized institute you have to take the entrance exams. After that you need to clear the GD and interview round also.</p>

