<h3>XAT Eligibility</h3>
 
<p>XAT Eligibility Criteria 2019, a candidate is eligible to appear in XAT 2019, if he/she holds recognized Bachelor's Degree with minimum 3 years duration or equivalent in any discipline</p>
 
<p>Those completing their final graduation exam by June 10, 2019 may also apply. XAT 2019 eligibility criteria further clarify that candidates in final year of Graduation are also eligible to apply for XAT 2018.</p>
 
<p>XAT eligibility does not impose any age bar to appear in XAT 2019 exam</p>
 
