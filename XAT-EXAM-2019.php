<p>Xavier Aptitude Test (XAT) is national-level management aptitude test. It is conducted by Xavier School of Management. XLRI conducts the exam on behalf of Xavier Association of Management Institutes (XAMI).. For more than 60 years XLRI is conducting XAT at all India level to select the most appropriate students for management education. The XAT score is being used by more than 150 institutes for the admission.</p>

