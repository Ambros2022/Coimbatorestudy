				<h1><font color="#0191d5">Why Public  </font> Schools</h1>
				
<div class="rrrppp">	
<ul>
<li>Public schools are the only schools that must meet the needs of all students. They do not turn children or families away. Public schools serve children with physical, emotional, and mental disabilities, those who are extremely gifted and those who are learning challenged, right along with children without special needs.</li>
<li> Public schools foster interactions and understanding among people of different ethnic, religious, and socioeconomic backgrounds.</li>
<li> Public schools are cheaper and more affordable than Residential schools.</li>
<li> Parents who send their child to public school say they love the proximity—some can walk to class—and the sense of community</li>
<li> Your choices are broadened.Some districts will allow you to transfer to a school in another neighborhood that has open seats.</li>
<li> Parents have a voice and involvement.      You can join the PTA or PTO and there are other ways to get involved. Many schools have advisory councils, where elected parents, teachers, and community members have a voice in decisions such as the hiring of a new principal. </li>
<li> Public school teachers follow a curriculum dictated by the state and local district. While some parents long for their child to be in a free-spirited environment, others praise the structure in public schools, which are held accountable through their test scores. Speaking of test scores, school-wide averages are publicly available and can give you an idea of the level of achievement.</li>
<li> Public schools are required to provide special education services to eligible students. Bright students may be eligible for the gifted program. Parents sometimes have to lobby hard to access the services their kids need, but the quality of such services is often top notch.</li>
<li> Your child will probably get a free lift to school compliments of the school bus.</li>
<li> Less Competition in Public Schools make a child stress free.</li>
</ul>
