				<h1><font color="#0191d5">Public </font> Schools </h1>
				
<p>Public schools are state schools, funded and operated by the state.Generally primary or secondary schools mandated for or offered to all children without charge, funded in whole or in part by taxation. These schools are generally inclusive (non-selective) in admitting all students within the geographical area that they serve. 
Public schools have to serve every child in a community. They don't get to cherry-pick only the brightest or wealthiest students. Attend a public school, and you're getting to know people from every corner of your community, not just people of the same religion or social class. In public school, you're part of the, well, public.They're open to all and helping children from all races, ethnicities and economic classes. </p>

<p>Public schools generally have a range of children from the whole gamut of socioeconomic classes and a wide variety of backgrounds. Public school is an opportunity to meet it and learn to negotiate with other points of view an understand people with diverse backgrounds and values.</p>

<p>As with ethnic, cultural, and socioeconomic backgrounds, the diversity introduces students to the communication issues and interpersonal issues that rubbing elbows with people who are different from oneself provides.</p>

<p>Public schools expose students to a variety of teachers: even in situations with one main classroom teacher, students may have additional instructors for foreign language, home economics, shop, physical education, drama, music, art, etc. This gives them an opportunity to learn with diverse pedagogies.</p>

