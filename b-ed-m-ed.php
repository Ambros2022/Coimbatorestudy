				<h1><font color="#0191d5">Bachelor of Education </font> (B.Ed) </h1>
				<p>Bachelor of Education (B.Ed) is a course offered for those interested in pursuing career in teaching. The B.Ed degree is mandatory for teaching in higher primary schools and high schools. The minimum qualification required for entry into B.Ed. course is Bachelor of Arts (B.A.), Bachelor of Science (B.Sc.) or Bachelor of Commerce (B.Com). While students from arts stream are trained to teach subjects like history, civics, geography and languages. The students from science stream are trained to teach mathematics, physics, chemistry and biology. After B.Ed., students can pursue Master in Education (M.Ed.) in Indian universities. National Council for Teacher Education is statutory body which regulates courses in teaching in India. </p>
				<p>There is a course called Diploma in Education (D.Ed.) However, the qualification for admission to this course is pass in pre-university course.The duration of the course is total two years. The Right of Free and Compulsory Education for Children (RTE Act-2009) came into force as an act from April 2010. The act emphasis on the quality education for every child. Therefore, to improve the quality in education, the quality of teachers must be a crucial factor. With a view to bring quality in education, the entry level percentage to B. Ed has been fixed at 45 percent for Scheduled Cast and Scheduled Tribes 50 percent for general.</p>
				<p>There are lots of opportunities with an education degree. If it is for teaching school aged children, think of all the things children are involved in besides school. A good place to look for these sorts of jobs would be with the city since in most places it is the city that runs community centres and so on. Also, remember that teaching doesn't have to mean in a classroom at a school. It can be a useful degree for HR positions at many companies where perhaps one could be involved in the hiring and training of new employees. Many companies look at the fact that you have a degree as impressive even if it is not related to the field. A degree helps show that you are a hard worker and that you have the ability to learn.</p>
