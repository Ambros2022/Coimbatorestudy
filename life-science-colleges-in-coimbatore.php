<p>			Modern medicine is marching along in great big strides, riding on the incredible success that biochemistry has enjoyed for about a century now. This interdisciplinary study reduces biological processes (reproduction, aging, genetics) to their chemical identity for easier comprehension. It's not all internal though; external environmental influences have large effect on all living things and biochemists must tackle that mystery as well. The biochemist must embrace a large field of study at both micro-levels (studying carbohydrates, enzymes and fats and their functions at the cellular level) and macro-levels (studying respiration, metabolism) and more or less everything else related to the biological process.</p>

